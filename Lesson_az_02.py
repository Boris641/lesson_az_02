import pandas as pd
import numpy as np

# Список имен учеников
students = ["Ольга", "Лариса", "Владимир", "Сергей", "Татьяна", "Наталья", "Виктор", "Михаил", "Оксана", "Светлана"]

# Список предметов
subjects = ["алгебра", "геометрия", "физика", "химия", "история"]

# Генерация случайных оценок от 2 до 5 для каждого ученика по каждому предмету
grades = np.random.randint(2, 6, (10, 5))

# Создание DataFrame
df = pd.DataFrame(grades, columns=subjects, index=students)
print("Оценки учеников:\n", df)

# Вычисление средней оценки по каждому предмету
mean_grades = df.mean()
print("\nСредняя оценка по каждому предмету:\n", mean_grades)

# Вычисление медианной оценки по каждому предмету
median_grades = df.median()
print("\nМедианная оценка по каждому предмету:\n", median_grades)

# Вычисление Q1 и Q3 для каждого предмета
Q1 = df.quantile(0.25)
Q3 = df.quantile(0.75)
print("\nПервый квартиль (Q1) по каждому предмету:\n", Q1)
print("\nТретий квартиль (Q3) по каждому предмету:\n", Q3)

# Вычисление IQR (межквартильный размах) для каждого предмета
IQR = Q3 - Q1
print("\nМежквартильный размах (IQR) по каждому предмету:\n", IQR)

# Вычисление стандартного отклонения по каждому предмету
std_deviation = df.std()
print("\nСтандартное отклонение по каждому предмету:\n", std_deviation)





